private void addMember(String name) {
        String role = (String) jComboBox1.getSelectedItem(); // Get the selected role
    try (Connection con = DriverManager.getConnection(DB_URL, DB_USER, DB_PASS)) {
        String query = "INSERT INTO members (name, role) VALUES (?, ?)";
        PreparedStatement pst = con.prepareStatement(query);
        pst.setString(1, name);
        pst.setString(2, role); // Set the role in the prepared statement
        pst.executeUpdate();
        JOptionPane.showMessageDialog(this, "Member added successfully");
        loadMembers(); // Reload members list
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error adding member");
    }
}
