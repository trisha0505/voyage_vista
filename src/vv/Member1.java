private void loadMembers() {
        try (Connection con = DriverManager.getConnection(DB_URL, DB_USER, DB_PASS)) {
        String query = "SELECT * FROM members";
        Statement stmt = con.createStatement();
        ResultSet rs = stmt.executeQuery(query);
        jTextArea1.setText(""); // Clear previous entries
        while (rs.next()) {
            String memberName = rs.getString("name");
            String memberRole = rs.getString("role"); // Get the member role
            jTextArea1.append(memberName + " - " + memberRole + "\n"); // Display name and role
        }
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error loading members");
    }
    }
